# Solicitando as notas do usuário
primeiro_bim = float(input('Insira as notas do primeiro semestre: '))
segundo_bim = float(input('Insira as notas do segundo semestre: '))
terceiro_bim = float(input('Insira as notas do terceiro semestre: '))
quarto_bim = float(input('Insira as notas do quarto semestre: '))

# Calculando a média das notas
media = (primeiro_bim + segundo_bim + terceiro_bim + quarto_bim) / 4

# Exibindo a média calculada
print('A média das notas é:')
print(media)



 